package webapi

import (
    "net/http"
    
    application "wellbe-common/application"

    constants "wellbe-common/share/commonsettings/constants"
    messages "wellbe-common/share/messages"
    "fmt"
    "strconv"
    "github.com/gin-gonic/gin"
)


type CMenuLabelWebApi interface {
    CreateAccessPoint(r *gin.Engine)*gin.Engine
}

type cMenuLabelWebApi struct {
    cMenuLabelApplication application.CMenuLabelApplication
}

func NewCMenuLabelWebApi(la application.CMenuLabelApplication) CMenuLabelWebApi {
    return &cMenuLabelWebApi{
        cMenuLabelApplication :la,
    }
}


func (la cMenuLabelWebApi) CreateAccessPoint(r *gin.Engine)*gin.Engine{
    r.GET("/c_menu_labels/key", la.GetCMenuLabelWithKey())
    r.GET("/c_menu_labels/language_cd", la.GetCMenuLabelWithLanguageCd())

    return r
}

type CMenuLabelEntity struct {
    MenuLabelCd string `json:"menu_label_cd"`
    LanguageCd string `json:"language_cd"`
    MenuLabelName string `json:"menu_label_name"`
    CreateDatetime string `json:"create_datetime"`
    CreateFunction string `json:"create_function"`
    UpdateDatetime string `json:"update_datetime"`
    UpdateFunction string `json:"update_function"`
}

func (la cMenuLabelWebApi)GetCMenuLabelWithKey() gin.HandlerFunc {
    return func(c *gin.Context) {
        ctx := c.Request.Context()
        key := c.Request.Header.Get(constants.API_KEY_REUQEST_HEADER_NAME)
        if key != constants.API_KEY_CLIENT {
            c.JSON(http.StatusUnauthorized, gin.H{})
            return
        }

        menuLabelCd_bind := c.Query("menu_label_cd")
        if len(menuLabelCd_bind) == 0 {
            c.JSON(http.StatusBadRequest, gin.H{constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: fmt.Sprintf(messages.MESSAGE_EN_REQUEST_ITEM_MANDATORY, "menu_label_cd")})
            return
        }
        menuLabelCd, _ := strconv.Atoi(menuLabelCd_bind)
        results, err := la.cMenuLabelApplication.GetCMenuLabelWithKey(&ctx, menuLabelCd)
        if err != nil {
            if err.Code >= 900 {
                c.JSON(http.StatusInternalServerError, gin.H{
                    constants.WEBAPI_RESPONSE_KEYWORD_STATUS:constants.LOGIC_ERROR_CODE_SEVERERROR,
                    constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: messages.MESSAGE_EN_SERVER_ERROR,
                })
            } else {
                c.JSON(http.StatusInternalServerError, gin.H{
                    constants.WEBAPI_RESPONSE_KEYWORD_STATUS: err.Code,
                    constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: err.Msg,
                })
            }
            return
        }

        c.JSON(http.StatusOK, gin.H{
            constants.WEBAPI_RESPONSE_KEYWORD_STATUS:constants.LOGIC_ERROR_CODE_SUCCESS,
            constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: messages.MESSAGE_EN_SUCCESS,
            "c_menu_labels": results,
        })
    }
}

