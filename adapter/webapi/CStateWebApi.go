package webapi

import (
    "net/http"
    
    application "wellbe-common/application"

    constants "wellbe-common/share/commonsettings/constants"
    messages "wellbe-common/share/messages"
    "fmt"
    "strconv"
    "github.com/gin-gonic/gin"
)


type CStateWebApi interface {
    CreateAccessPoint(r *gin.Engine)*gin.Engine
}

type cStateWebApi struct {
    cStateApplication application.CStateApplication
}

func NewCStateWebApi(la application.CStateApplication) CStateWebApi {
    return &cStateWebApi{
        cStateApplication :la,
    }
}


func (la cStateWebApi) CreateAccessPoint(r *gin.Engine)*gin.Engine{
    r.GET("/c_states/key", la.GetCStateWithKey())
    r.GET("/c_states/state_cd_iso", la.GetCStateWithStateCdIso())
    r.GET("/c_states/language_cd", la.GetCStateWithLanguageCd())
    r.GET("/c_states/country_cd", la.GetCStateWithCountryCd())
    r.GET("/c_states/state_cd", la.GetCStateWithStateCd())

    return r
}

type CStateEntity struct {
    StateCd string `json:"state_cd"`
    LanguageCd string `json:"language_cd"`
    CountryCd string `json:"country_cd"`
    StateName string `json:"state_name"`
    StateCdIso string `json:"state_cd_iso"`
    TimezoneIana string `json:"timezone_iana"`
    CreateDatetime string `json:"create_datetime"`
    CreateFunction string `json:"create_function"`
    UpdateDatetime string `json:"update_datetime"`
    UpdateFunction string `json:"update_function"`
}

func (la cStateWebApi)GetCStateWithKey() gin.HandlerFunc {
    return func(c *gin.Context) {
        ctx := c.Request.Context()
        key := c.Request.Header.Get(constants.API_KEY_REUQEST_HEADER_NAME)
        if key != constants.API_KEY_CLIENT {
            c.JSON(http.StatusUnauthorized, gin.H{})
            return
        }

        stateCd_bind := c.Query("state_cd")
        languageCd_bind := c.Query("language_cd")
        if len(stateCd_bind) == 0 {
            c.JSON(http.StatusBadRequest, gin.H{constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: fmt.Sprintf(messages.MESSAGE_EN_REQUEST_ITEM_MANDATORY, "state_cd")})
            return
        }
        if len(languageCd_bind) == 0 {
            c.JSON(http.StatusBadRequest, gin.H{constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: fmt.Sprintf(messages.MESSAGE_EN_REQUEST_ITEM_MANDATORY, "language_cd")})
            return
        }
        stateCd, _ := strconv.Atoi(stateCd_bind)
        languageCd, _ := strconv.Atoi(languageCd_bind)
        results, err := la.cStateApplication.GetCStateWithKey(&ctx, stateCd,languageCd)
        if err != nil {
            if err.Code >= 900 {
                c.JSON(http.StatusInternalServerError, gin.H{
                    constants.WEBAPI_RESPONSE_KEYWORD_STATUS:constants.LOGIC_ERROR_CODE_SEVERERROR,
                    constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: messages.MESSAGE_EN_SERVER_ERROR,
                })
            } else {
                c.JSON(http.StatusInternalServerError, gin.H{
                    constants.WEBAPI_RESPONSE_KEYWORD_STATUS: err.Code,
                    constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: err.Msg,
                })
            }
            return
        }

        c.JSON(http.StatusOK, gin.H{
            constants.WEBAPI_RESPONSE_KEYWORD_STATUS:constants.LOGIC_ERROR_CODE_SUCCESS,
            constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: messages.MESSAGE_EN_SUCCESS,
            "c_states": results,
        })
    }
}

