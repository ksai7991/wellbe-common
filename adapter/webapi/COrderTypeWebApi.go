package webapi

import (
    "net/http"
    
    application "wellbe-common/application"

    constants "wellbe-common/share/commonsettings/constants"
    messages "wellbe-common/share/messages"
    "fmt"
    "strconv"
    "github.com/gin-gonic/gin"
)


type COrderTypeWebApi interface {
    CreateAccessPoint(r *gin.Engine)*gin.Engine
}

type cOrderTypeWebApi struct {
    cOrderTypeApplication application.COrderTypeApplication
}

func NewCOrderTypeWebApi(la application.COrderTypeApplication) COrderTypeWebApi {
    return &cOrderTypeWebApi{
        cOrderTypeApplication :la,
    }
}


func (la cOrderTypeWebApi) CreateAccessPoint(r *gin.Engine)*gin.Engine{
    r.GET("/c_order_types/key", la.GetCOrderTypeWithKey())
    r.GET("/c_order_types/language_cd", la.GetCOrderTypeWithLanguageCd())

    return r
}

type COrderTypeEntity struct {
    OrderTypeCd string `json:"order_type_cd"`
    LanguageCd string `json:"language_cd"`
    OrderTypeName string `json:"order_type_name"`
    CreateDatetime string `json:"create_datetime"`
    CreateFunction string `json:"create_function"`
    UpdateDatetime string `json:"update_datetime"`
    UpdateFunction string `json:"update_function"`
}

func (la cOrderTypeWebApi)GetCOrderTypeWithKey() gin.HandlerFunc {
    return func(c *gin.Context) {
        ctx := c.Request.Context()
        key := c.Request.Header.Get(constants.API_KEY_REUQEST_HEADER_NAME)
        if key != constants.API_KEY_CLIENT {
            c.JSON(http.StatusUnauthorized, gin.H{})
            return
        }

        orderTypeCd_bind := c.Query("order_type_cd")
        if len(orderTypeCd_bind) == 0 {
            c.JSON(http.StatusBadRequest, gin.H{constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: fmt.Sprintf(messages.MESSAGE_EN_REQUEST_ITEM_MANDATORY, "order_type_cd")})
            return
        }
        orderTypeCd, _ := strconv.Atoi(orderTypeCd_bind)
        results, err := la.cOrderTypeApplication.GetCOrderTypeWithKey(&ctx, orderTypeCd)
        if err != nil {
            if err.Code >= 900 {
                c.JSON(http.StatusInternalServerError, gin.H{
                    constants.WEBAPI_RESPONSE_KEYWORD_STATUS:constants.LOGIC_ERROR_CODE_SEVERERROR,
                    constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: messages.MESSAGE_EN_SERVER_ERROR,
                })
            } else {
                c.JSON(http.StatusInternalServerError, gin.H{
                    constants.WEBAPI_RESPONSE_KEYWORD_STATUS: err.Code,
                    constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: err.Msg,
                })
            }
            return
        }

        c.JSON(http.StatusOK, gin.H{
            constants.WEBAPI_RESPONSE_KEYWORD_STATUS:constants.LOGIC_ERROR_CODE_SUCCESS,
            constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: messages.MESSAGE_EN_SUCCESS,
            "c_order_types": results,
        })
    }
}

