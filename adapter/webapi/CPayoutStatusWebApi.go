package webapi

import (
    "net/http"
    
    application "wellbe-common/application"

    constants "wellbe-common/share/commonsettings/constants"
    messages "wellbe-common/share/messages"
    "fmt"
    "strconv"
    "github.com/gin-gonic/gin"
)


type CPayoutStatusWebApi interface {
    CreateAccessPoint(r *gin.Engine)*gin.Engine
}

type cPayoutStatusWebApi struct {
    cPayoutStatusApplication application.CPayoutStatusApplication
}

func NewCPayoutStatusWebApi(la application.CPayoutStatusApplication) CPayoutStatusWebApi {
    return &cPayoutStatusWebApi{
        cPayoutStatusApplication :la,
    }
}


func (la cPayoutStatusWebApi) CreateAccessPoint(r *gin.Engine)*gin.Engine{
    r.GET("/c_payout_statuss/key", la.GetCPayoutStatusWithKey())
    r.GET("/c_payout_statuss/language_cd", la.GetCPayoutStatusWithLanguageCd())

    return r
}

type CPayoutStatusEntity struct {
    PayoutStatusCd string `json:"payout_status_cd"`
    LanguageCd string `json:"language_cd"`
    PayoutStatusName string `json:"payout_status_name"`
    CreateDatetime string `json:"create_datetime"`
    CreateFunction string `json:"create_function"`
    UpdateDatetime string `json:"update_datetime"`
    UpdateFunction string `json:"update_function"`
}

func (la cPayoutStatusWebApi)GetCPayoutStatusWithKey() gin.HandlerFunc {
    return func(c *gin.Context) {
        ctx := c.Request.Context()
        key := c.Request.Header.Get(constants.API_KEY_REUQEST_HEADER_NAME)
        if key != constants.API_KEY_CLIENT {
            c.JSON(http.StatusUnauthorized, gin.H{})
            return
        }

        payoutStatusCd_bind := c.Query("payout_status_cd")
        if len(payoutStatusCd_bind) == 0 {
            c.JSON(http.StatusBadRequest, gin.H{constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: fmt.Sprintf(messages.MESSAGE_EN_REQUEST_ITEM_MANDATORY, "payout_status_cd")})
            return
        }
        payoutStatusCd, _ := strconv.Atoi(payoutStatusCd_bind)
        results, err := la.cPayoutStatusApplication.GetCPayoutStatusWithKey(&ctx, payoutStatusCd)
        if err != nil {
            if err.Code >= 900 {
                c.JSON(http.StatusInternalServerError, gin.H{
                    constants.WEBAPI_RESPONSE_KEYWORD_STATUS:constants.LOGIC_ERROR_CODE_SEVERERROR,
                    constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: messages.MESSAGE_EN_SERVER_ERROR,
                })
            } else {
                c.JSON(http.StatusInternalServerError, gin.H{
                    constants.WEBAPI_RESPONSE_KEYWORD_STATUS: err.Code,
                    constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: err.Msg,
                })
            }
            return
        }

        c.JSON(http.StatusOK, gin.H{
            constants.WEBAPI_RESPONSE_KEYWORD_STATUS:constants.LOGIC_ERROR_CODE_SUCCESS,
            constants.WEBAPI_RESPONSE_KEYWORD_MESSAGE: messages.MESSAGE_EN_SUCCESS,
            "c_payout_statuss": results,
        })
    }
}

